    <p>2.At least 17 years old.</p>
    <p>3.Weight at least 50 Kilogram.</p>
   <p> 4.The donation interval after a blood donation is generally 60 days,
      every 7 days for plasma and every 14 days for platelets.</p>
<h3 style=font-size:24px>Some common reasons for temporary deferral:- </h3>
   <p> 1.Cold, flu or sore throat, please wait one week after recovery or treatment.</p>
    <p>2.Tooth extraction or dental work, please wait 3 days after treatment.</p>
    <p>3.Skin infections (minor), wait 1 week after complete healing.</p>
    <p>4.Close contact with Hepatitis B, wait 12 months and after full course of 
      hepatitis B vaccination (and shown a satisfactory antibody response).</p>
